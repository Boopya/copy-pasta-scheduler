class Project:
    def __init__(self, id, title, size, priority):
        self.id = id
        self.title = title
        self.size = size
        self.priority = priority

    def getId():
        return id
    
    def getTitle():
        return title
    
    def getSize():
        return size
    
    def getPriority():
        return priority

    def setTitle(title):
        self.title = title

    def setSize(size):
        self.size = size
    
    def setPriority(priority):
        self.priority = priority